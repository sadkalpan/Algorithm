class FloatDivision {
	public static void main(String args[]) throws Exception
	{
		double x, y, z;

		x = 23.5;
		System.out.println(" x = " + x);
		y = 7.3;
		System.out.println(" y = " + y );

		z = x / y;
		System.out.println(" x / y = " + z );
		z = x % y;
		System.out.println(" x % y = " + z);
	}
}
